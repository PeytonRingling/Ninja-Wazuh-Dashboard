// ── Exported rule detail drawer (used from other tabs) ────────────────────────

export function RuleDetailDrawer({ ruleId, description = "", level = 0, hoursBack = 24, zBase = 60, onClose }: {
  ruleId: string;
  description?: string;
  level?: number;
  hoursBack?: number;
  zBase?: number;
  onClose: () => void;
}) {
  const [rule, setRule] = useState<NoisyRule>({
    rule_id: ruleId, description, level,
    severity: level >= 12 ? "critical" : level >= 8 ? "high" : level >= 4 ? "medium" : "low",
    alert_count: 0, last_triggered: "",
  });

  useEffect(() => {
    // Try to get accurate alert_count from noisy rules
    api.wazuhNoisyRules(hoursBack)
      .then(rules => { const found = rules.find(r => r.rule_id === ruleId); if (found) setRule(found); })
      .catch(() => {});
  }, [ruleId, hoursBack]);

  return <RuleDrawer rule={rule} hoursBack={hoursBack} allRules={[]} totalAlerts={0} zBase={zBase} onClose={onClose} />;
}

// ── Suppression Candidates Tab ────────────────────────────────────────────────

function SuppressTab({ allRules, checkedIds, onToggle, totalAlerts }: {
  allRules: NoisyRule[];
  checkedIds: Set<string>;
  onToggle: (id: string) => void;
  totalAlerts: number;
}) {
  const [xmlOut, setXmlOut] = useState<string | null>(null);
  const selected = allRules.filter(r => checkedIds.has(r.rule_id));

  const handleCopyAll = () => {
    selected.forEach(r => {
      const reductionPct = totalAlerts > 0 ? (r.alert_count / totalAlerts) * 100 : undefined;
      api.addSuppressionLog({ rule_id: r.rule_id, description: r.description, alert_count: r.alert_count, reduction_pct: reductionPct, notes: undefined, total_alerts: totalAlerts }).catch(() => {});
    });
  };

  const generateXml = () => {
    if (!selected.length) return;
    setXmlOut(selected.map((r, i) => buildSuppressXml(r.rule_id, r.description, i)).join("\n\n"));
  };

  return (
    <div className="space-y-4">
      {/* Action bar */}
      <div className="flex items-center justify-between gap-3 flex-wrap">
        <div>
          <p className="text-sm font-medium text-slate-300">
            {selected.length > 0
              ? `${selected.length} rule${selected.length !== 1 ? "s" : ""} selected for suppression`
              : "Check rules in the Noisy Rules tab to add them here"}
          </p>
          {selected.length > 0 && totalAlerts > 0 && (
            <p className="text-xs text-slate-500 mt-0.5">
              Est. alert reduction:{" "}
              <span className="text-accent font-semibold">
                {Math.round((selected.reduce((s, r) => s + r.alert_count, 0) / totalAlerts) * 100)}%
              </span>{" "}of current volume
            </p>
          )}
        </div>
        <button
          onClick={generateXml}
          disabled={!selected.length}
          className="flex items-center gap-2 px-4 py-2 rounded-lg text-sm font-semibold transition-all
            text-white disabled:opacity-40 disabled:cursor-not-allowed"
          style={{ background: "linear-gradient(135deg, #7c3aed, #a855f7)", boxShadow: "0 0 20px rgba(124,58,237,0.4)" }}
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M17.25 6.75L22.5 12l-5.25 5.25m-10.5 0L1.5 12l5.25-5.25m7.5-3l-4.5 16.5" />
          </svg>
          Generate suppression rules for selected
        </button>
      </div>

      {xmlOut && (
        <div className="bg-surface-800 rounded-xl border border-accent/20 overflow-hidden">
          <div className="flex items-center justify-between px-4 py-3 border-b border-surface-600">
            <span className="text-xs font-semibold text-slate-300">
              {selected.length} rule{selected.length !== 1 ? "s" : ""} · IDs {100900}–{100900 + selected.length - 1}
            </span>
            <CopyButton text={xmlOut} label="Copy all" onCopy={handleCopyAll} />
          </div>
          <pre className="text-[11px] font-mono text-slate-300 p-4 leading-relaxed whitespace-pre overflow-x-auto max-h-80">{xmlOut}</pre>
          <div className="px-4 py-2.5 border-t border-surface-600 bg-surface-900/40 text-[10px] text-slate-600">
            Paste into <span className="font-mono text-slate-500">/var/ossec/etc/rules/local_rules.xml</span>
          </div>
        </div>
      )}

      {allRules.length > 0 ? (
        <div className="bg-surface-800 rounded-xl border border-surface-600 overflow-hidden">
          <div className="flex items-center gap-3 px-4 py-3 border-b border-surface-600">
            <button onClick={() => allRules.forEach(r => { if (!checkedIds.has(r.rule_id)) onToggle(r.rule_id); })}
              className="text-xs text-accent hover:text-white font-medium transition-colors">Select all</button>
            <span className="text-slate-700">·</span>
            <button onClick={() => Array.from(checkedIds).forEach(id => onToggle(id))}
              className="text-xs text-slate-500 hover:text-slate-300 transition-colors">Clear all</button>
          </div>
          <div className="divide-y divide-surface-700/50">
            {allRules.map(r => {
              const pct = totalAlerts > 0 ? Math.round((r.alert_count / totalAlerts) * 100) : 0;
              const isChecked = checkedIds.has(r.rule_id);
              return (
                <label key={r.rule_id}
                  className={`flex items-center gap-3 px-4 py-3 cursor-pointer transition-colors border-l-2 ${
                    isChecked ? "bg-accent/5 border-accent" : "border-transparent hover:bg-surface-700/30"
                  }`}>
                  <input type="checkbox" checked={isChecked} onChange={() => onToggle(r.rule_id)}
                    className="w-4 h-4 rounded border-surface-500 bg-surface-700 text-accent focus:ring-0 shrink-0" />
                  <span className="font-mono text-xs text-accent shrink-0 w-14">{r.rule_id}</span>
                  <span className="text-sm text-slate-300 flex-1 truncate min-w-0" title={r.description}>{r.description}</span>
                  <SevBadge severity={r.severity} label={String(r.level)} />
                  <div className="flex items-center gap-1.5 shrink-0">
                    <span className="text-xs font-semibold text-slate-300 tabular-nums">{r.alert_count.toLocaleString()}</span>
                    {pct > 0 && <span className="text-[10px] text-slate-600">({pct}%)</span>}
                  </div>
                </label>
              );
            })}
          </div>
        </div>
      ) : (
        <div className="text-slate-500 text-sm text-center py-12">No rules loaded</div>
      )}
    </div>
  );
}

// ── CSV export ────────────────────────────────────────────────────────────────

function exportRulesCSV(rules: NoisyRule[]) {
  const headers = ["Rule ID", "Description", "Severity", "Level", "Alert Count"];
  const rows = rules.map(r => [r.rule_id, r.description, r.severity, String(r.level), String(r.alert_count)]);
  const csv = [headers, ...rows].map(r => r.map(v => `"${String(v).replace(/"/g, '""')}"`).join(",")).join("\n");
  const blob = new Blob([csv], { type: "text/csv" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url; a.download = "noisy_rules.csv"; a.click();
  URL.revokeObjectURL(url);
}

// ── Main component ────────────────────────────────────────────────────────────

export default function NoisyRules({ data, error, hoursBack }: Props) {
  const [activeTab, setActiveTab]   = useState<"rules" | "candidates">("rules");
  const [sortKey, setSortKey]       = useState<SortKey>("alert_count");
  const [sortDir, setSortDir]       = useState<"asc" | "desc">("desc");
  const [sevFilter, setSevFilter]   = useState<string>("");
  const [search, setSearch]         = useState("");
  const [checkedIds, setCheckedIds] = useState<Set<string>>(new Set());
  const [drawerRule, setDrawerRule] = useState<NoisyRule | null>(null);
  const [quickSuppress, setQuickSuppress] = useState<string | null>(null);
  const [prevAlerts, setPrevAlerts] = useState<Map<string, number>>(new Map());

  // Fetch previous period data to compute trends (48h → prev period = 48h total minus 24h current)
  useEffect(() => {
    if (!data?.length) return;
    api.wazuhNoisyRules(hoursBack * 2).then(prevData => {
      const map = new Map<string, number>();
      for (const r of prevData) {
        const curCount = data.find(d => d.rule_id === r.rule_id)?.alert_count ?? 0;
        const prevCount = r.alert_count - curCount;
        if (prevCount > 0) map.set(r.rule_id, prevCount);
      }
      setPrevAlerts(map);
    }).catch(() => {});
  }, [data, hoursBack]);

  const handleSort = (key: SortKey) => {
    if (sortKey === key) setSortDir(d => d === "asc" ? "desc" : "asc");
    else { setSortKey(key); setSortDir("desc"); }
  };

  const toggleCheck = useCallback((id: string) => {
    setCheckedIds(prev => {
      const next = new Set(prev);
      next.has(id) ? next.delete(id) : next.add(id);
      return next;
    });
  }, []);

  const sevCounts = data
    ? SEV_ORDER.reduce((acc, s) => { acc[s] = data.filter(r => r.severity === s).length; return acc; }, {} as Record<string, number>)
    : null;

  const filtered = data
    ? [...data]
        .filter(r => !sevFilter || r.severity === sevFilter)
        .filter(r => !search.trim() || r.rule_id.includes(search.trim())
          || r.description.toLowerCase().includes(search.trim().toLowerCase()))
        .sort((a, b) => {
          const av = a[sortKey] ?? 0, bv = b[sortKey] ?? 0;
          const cmp = av < bv ? -1 : av > bv ? 1 : 0;
          return sortDir === "asc" ? cmp : -cmp;
        })
    : null;

  const totalAlerts = data?.reduce((s, r) => s + r.alert_count, 0) ?? 0;
  const maxCount    = filtered?.reduce((m, r) => Math.max(m, r.alert_count), 1) ?? 1;

  const SortIcon = ({ k }: { k: SortKey }) => (
    <span className={`ml-1 ${sortKey === k ? "text-accent" : "text-slate-600"}`}>
      {sortKey === k ? (sortDir === "asc" ? "↑" : "↓") : "↕"}
    </span>
  );

  const allVisibleChecked  = !!filtered?.length && filtered.every(r => checkedIds.has(r.rule_id));
  const someVisibleChecked = !!filtered?.some(r => checkedIds.has(r.rule_id));

  return (
    <div className="card">
      {/* Tab bar */}
      <div className="flex items-center gap-1 -mx-5 px-5 border-b border-surface-600 mb-5">
        {([
          ["rules",      "Noisy Rules"],
          ["candidates", `Suppression Candidates${checkedIds.size > 0 ? ` (${checkedIds.size})` : ""}`],
        ] as const).map(([tab, label]) => (
          <button key={tab} onClick={() => setActiveTab(tab)}
            className={`pb-3 pt-1 px-4 text-sm font-semibold border-b-2 transition-colors -mb-px whitespace-nowrap ${
              activeTab === tab ? "border-accent text-accent" : "border-transparent text-slate-500 hover:text-slate-300"
            }`}>
            {label}
          </button>
        ))}
      </div>

      {activeTab === "candidates" ? (
        <SuppressTab allRules={data ?? []} checkedIds={checkedIds} onToggle={toggleCheck} totalAlerts={totalAlerts} />
      ) : (
        <>
          {/* Controls */}
          <div className="flex flex-wrap items-center gap-3 mb-5">
            {/* Search */}
            <div className="relative flex-1 min-w-[200px]">
              <svg className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-500" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z" />
              </svg>
              <input type="text" value={search} onChange={e => setSearch(e.target.value)}
                placeholder="Search rule ID or description…"
                className="w-full pl-9 pr-8 py-2.5 bg-surface-700 border border-surface-600 rounded-lg text-sm text-slate-200 placeholder-slate-500 focus:outline-none focus:border-accent/60 transition-colors" />
              {search && (
                <button onClick={() => setSearch("")} className="absolute right-3 top-1/2 -translate-y-1/2 text-slate-500 hover:text-slate-300">
                  <svg className="w-3.5 h-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                    <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
                  </svg>
                </button>
              )}
            </div>

            {/* Severity filters */}
            {sevCounts && (
              <div className="flex flex-wrap gap-2">
                <button onClick={() => setSevFilter("")}
                  className={`px-3.5 py-2 rounded-lg text-sm font-semibold border-2 transition-colors ${
                    !sevFilter ? "bg-accent/20 text-accent border-accent/40" : "text-slate-400 border-surface-600 hover:text-slate-200 hover:border-surface-500"
                  }`}>
                  All
                </button>
                {SEV_ORDER.map(sev => {
                  const count = sevCounts[sev];
                  if (sev !== "critical" && count === 0) return null;
                  const active = sevFilter === sev;
                  return (
                    <button key={sev} onClick={() => setSevFilter(active ? "" : sev)}
                      className={`flex items-center gap-2 px-3.5 py-2 rounded-lg text-sm font-semibold border-2 transition-colors ${
                        active ? SEV_BTN_ACTIVE[sev] : "text-slate-400 border-surface-600 hover:text-slate-200 hover:border-surface-500"
                      }`}>
                      <span className="w-2 h-2 rounded-full shrink-0" style={{ background: SEV_DOT[sev] }} />
                      {SEV_LABELS[sev]}
                      <span className="text-xs opacity-60">({count})</span>
                    </button>
                  );
                })}
              </div>
            )}
          </div>

          {/* CSV export */}
          {filtered && filtered.length > 0 && (
            <button
              onClick={() => exportRulesCSV(filtered)}
              className="flex items-center gap-1.5 px-3 py-2.5 rounded-lg text-xs font-medium text-slate-300 hover:text-white border border-surface-600 hover:border-surface-500 bg-surface-700 hover:bg-surface-600 transition-all shrink-0"
            >
              <svg className="w-3.5 h-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
              </svg>
              Export CSV
            </button>
          )}

          {/* Subtitle */}
          <p className="text-xs text-slate-500 mb-4">
            Last {hoursBack}h
            {filtered && ` · ${filtered.length}${sevFilter ? ` ${sevFilter}` : ""} rule${filtered.length !== 1 ? "s" : ""}`}
            {search && ` matching "${search}"`}
            {checkedIds.size > 0 && <> · <span className="text-accent">{checkedIds.size} selected</span> — switch to Suppression Candidates to generate XML</>}
          </p>

          {error ? (
            <div className="text-red-400 text-sm py-6 text-center">{error}</div>
          ) : !filtered ? (
            <div className="space-y-2.5">{Array.from({ length: 8 }).map((_, i) => <div key={i} className="skeleton h-12 rounded-lg" />)}</div>
          ) : filtered.length === 0 ? (
            <div className="text-slate-500 text-sm text-center py-10">
              {search ? `No rules match "${search}"` : sevFilter ? `No ${sevFilter} rules` : "No rules found"}
            </div>
          ) : (
            <div className="overflow-x-auto">
              <table className="w-full text-sm">
                <thead>
                  <tr className="border-b border-surface-600">
                    <th className="py-3 px-3 w-10">
                      <input type="checkbox" checked={allVisibleChecked}
                        ref={el => { if (el) el.indeterminate = someVisibleChecked && !allVisibleChecked; }}
                        onChange={() => {
                          if (allVisibleChecked) filtered.forEach(r => { if (checkedIds.has(r.rule_id)) toggleCheck(r.rule_id); });
                          else filtered.forEach(r => { if (!checkedIds.has(r.rule_id)) toggleCheck(r.rule_id); });
                        }}
                        className="w-4 h-4 rounded border-surface-500 bg-surface-700 text-accent focus:ring-0 cursor-pointer" />
                    </th>
                    {([
                      ["rule_id",     "Rule ID"],
                      ["description", "Description"],
                      ["level",       "Severity"],
                      ["alert_count", "Alert Count"],
                    ] as [SortKey | "description", string][]).map(([key, label]) => (
                      <th key={key}
                        onClick={() => key !== "description" && handleSort(key as SortKey)}
                        className={`text-left py-3 px-3 text-xs font-semibold text-slate-400 whitespace-nowrap ${key !== "description" ? "cursor-pointer hover:text-slate-200" : ""}`}>
                        {label}{key !== "description" && <SortIcon k={key as SortKey} />}
                      </th>
                    ))}
                    <th className="py-3 px-3 w-14 text-center">
                      <span className="text-xs font-semibold text-slate-600">Trend</span>
                    </th>
                    <th className="py-3 px-3 w-28 text-right">
                      <span className="text-xs font-semibold text-slate-600">Suppress</span>
                    </th>
                  </tr>
                </thead>
                <tbody>
                  {filtered.map(rule => {
                    const pct = Math.round((rule.alert_count / maxCount) * 100);
                    const barColor   = heatColor(pct);
                    const isChecked  = checkedIds.has(rule.rule_id);
                    const isDrawer   = drawerRule?.rule_id === rule.rule_id;
                    const isQuick    = quickSuppress === rule.rule_id;
                    return (
                      <tr key={rule.rule_id}
                        className={`border-b border-surface-700/50 transition-colors ${
                          isDrawer ? "bg-accent/5" : isChecked ? "bg-surface-800/60" : "hover:bg-surface-700/30"
                        }`}>

                        <td className="py-3 px-3">
                          <input type="checkbox" checked={isChecked} onChange={() => toggleCheck(rule.rule_id)}
                            onClick={e => e.stopPropagation()}
                            className="w-4 h-4 rounded border-surface-500 bg-surface-700 text-accent focus:ring-0 cursor-pointer" />
                        </td>

                        <td className="py-3 px-3">
                          <button onClick={() => setDrawerRule(isDrawer ? null : rule)}
                            className="font-mono text-xs text-accent hover:text-white transition-colors flex items-center gap-1 group"
                            title="Open rule analysis">
                            {rule.rule_id}
                            <svg className={`w-3 h-3 text-slate-600 group-hover:text-accent transition-transform ${isDrawer ? "rotate-90" : ""}`}
                              fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                              <path strokeLinecap="round" strokeLinejoin="round" d="M8.25 4.5l7.5 7.5-7.5 7.5" />
                            </svg>
                          </button>
                        </td>

                        <td className="py-3 px-3 max-w-sm">
                          <button onClick={() => setDrawerRule(isDrawer ? null : rule)}
                            className="text-left truncate block text-slate-300 hover:text-white transition-colors w-full"
                            title={rule.description}>
                            {rule.description}
                          </button>
                        </td>

                        <td className="py-3 px-3">
                          <SevBadge severity={rule.severity} label={`${rule.level} · ${SEV_LABELS[rule.severity] ?? rule.severity}`} />
                        </td>

                        <td className="py-3 px-3">
                          <div className="flex items-center gap-2">
                            <div className="flex-1 h-1.5 bg-surface-600 rounded-full overflow-hidden min-w-[60px]">
                              <div className="h-full rounded-full" style={{ width: `${pct}%`, background: barColor }} />
                            </div>
                            <span className="font-semibold text-slate-200 tabular-nums min-w-[52px] text-right text-xs">
                              {rule.alert_count.toLocaleString()}
                            </span>
                          </div>
                        </td>

                        <td className="py-3 px-3 text-center w-14">
                          {(() => {
                            const prev = prevAlerts.get(rule.rule_id);
                            if (prev == null) return <span className="text-slate-700 text-xs">—</span>;
                            const pct = Math.round(((rule.alert_count - prev) / prev) * 100);
                            if (pct > 10) return (
                              <span className="text-[#ff6b35] text-base font-bold leading-none" title={`+${pct}% vs prev ${hoursBack}h`}>↑</span>
                            );
                            if (pct < -10) return (
                              <span className="text-[#34d399] text-base font-bold leading-none" title={`${pct}% vs prev ${hoursBack}h`}>↓</span>
                            );
                            return <span className="text-slate-500 text-xs" title={`${pct > 0 ? "+" : ""}${pct}% vs prev ${hoursBack}h`}>—</span>;
                          })()}
                        </td>

                        <td className="py-3 px-3 relative">
                          <button
                            onClick={e => { e.stopPropagation(); setQuickSuppress(prev => prev === rule.rule_id ? null : rule.rule_id); }}
                            className={`flex items-center gap-1.5 text-xs px-2.5 py-1.5 rounded-lg border transition-colors font-medium ${
                              isQuick ? "bg-accent/20 text-accent border-accent/30" : "text-slate-500 border-surface-600 hover:text-slate-200 hover:border-surface-500 hover:bg-surface-700"
                            }`}>
                            <svg className="w-3.5 h-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                              <path strokeLinecap="round" strokeLinejoin="round" d="M17.25 6.75L22.5 12l-5.25 5.25m-10.5 0L1.5 12l5.25-5.25m7.5-3l-4.5 16.5" />
                            </svg>
                            Suppress
                          </button>
                          {isQuick && (
                            <QuickSuppressPopover ruleId={rule.rule_id} description={rule.description} alertCount={rule.alert_count} totalAlerts={totalAlerts} onClose={() => setQuickSuppress(null)} />
                          )}
                        </td>
                      </tr>
                    );
                  })}
                </tbody>
              </table>
            </div>
          )}
        </>
      )}

      {drawerRule && (
        <RuleDrawer
          rule={drawerRule}
          hoursBack={hoursBack}
          allRules={data ?? []}
          totalAlerts={totalAlerts}
          onClose={() => setDrawerRule(null)}
        />
      )}
    </div>
  );
}
