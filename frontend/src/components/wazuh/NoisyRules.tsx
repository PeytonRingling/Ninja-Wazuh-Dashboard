// ── XML builder ───────────────────────────────────────────────────────────────

function xmlEscape(s: string) {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

function buildSuppressXml(ruleId: string, description: string, index = 0, notes = ""): string {
  const id = 100900 + index;
  const notesPart = notes.trim() ? ` - ${xmlEscape(notes.trim())}` : "";
  return `<rule id="${id}" level="0">
  <if_sid>${ruleId}</if_sid>
  <description>False positive - ${xmlEscape(description)}${notesPart} - suppressed ${TODAY}</description>
</rule>`;
}

