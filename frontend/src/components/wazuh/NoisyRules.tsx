// ── ParsedMessage ─────────────────────────────────────────────────────────────

const MSG_KEY_COLOR: Record<string, string> = {
  Image: "text-cyan-400", TargetFilename: "text-yellow-400", TargetImage: "text-yellow-400",
  User: "text-purple-400", CommandLine: "text-orange-400", ParentImage: "text-slate-400",
  DestinationIp: "text-red-400", DestinationPort: "text-red-400", SourceIp: "text-red-300",
};
const MSG_SKIP = new Set(["ProcessGuid","ParentProcessGuid","CreationUtcTime",
  "ProcessId","ParentProcessId","EventRecordID","ThreadID","UtcTime","RuleName"]);

function ParsedMessage({ message }: { message: string }) {
  const lines = message.trim().split(/\r?\n/);
  const heading = lines[0].replace(/:$/, "").trim();
  const pairs = lines.slice(1)
    .map(l => { const i = l.indexOf(":"); return i > -1 ? { key: l.slice(0, i).trim(), value: l.slice(i + 1).trim() } : null; })
    .filter((p): p is { key: string; value: string } => !!p?.key && !!p?.value && p.value !== "-" && !MSG_SKIP.has(p.key));
  return (
    <div>
      <p className="text-[10px] font-semibold text-accent mb-2">{heading}</p>
      <div className="space-y-1">
        {pairs.map(({ key, value }) => (
          <div key={key} className="flex gap-2 min-w-0 text-[11px]">
            <span className="text-slate-600 shrink-0 w-32 text-right">{key}</span>
            <span className={`font-mono break-all ${MSG_KEY_COLOR[key] ?? "text-slate-300"}`}>{value}</span>
          </div>
        ))}
        {!pairs.length && <span className="text-slate-600 text-[11px]">No parsed fields</span>}
      </div>
    </div>
  );
}

