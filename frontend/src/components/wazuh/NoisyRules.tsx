function describeRule(rule: NoisyRule, detail: WazuhRuleDetail | null): {
  text: string; category: string; categoryColor: string;
} {
  const groups   = detail?.groups ?? [];
  const filename = (detail?.filename ?? "").toLowerCase();

  let category = "";
  let categoryColor = "text-slate-400 bg-surface-700/60";

  if (filename.includes("syscheck") || groups.some(g => g.startsWith("syscheck"))) {
    category = "File Integrity (FIM)";  categoryColor = "text-yellow-400 bg-yellow-500/10 border-yellow-500/20";
  } else if (filename.includes("sysmon")) {
    category = "Sysmon Endpoint";       categoryColor = "text-cyan-400 bg-cyan-500/10 border-cyan-500/20";
  } else if (filename.includes("windows") || groups.some(g => g.includes("windows_event"))) {
    category = "Windows Event Log";     categoryColor = "text-blue-400 bg-blue-500/10 border-blue-500/20";
  } else if (groups.some(g => ["authentication","login","pam"].some(k => g.includes(k)))) {
    category = "Authentication";        categoryColor = "text-purple-400 bg-purple-500/10 border-purple-500/20";
  } else if (groups.some(g => ["web","apache","nginx","iis"].some(k => g.includes(k)))) {
    category = "Web Server";            categoryColor = "text-green-400 bg-green-500/10 border-green-500/20";
  } else if (groups.some(g => ["firewall","network","pf"].some(k => g.includes(k)))) {
    category = "Network / Firewall";    categoryColor = "text-red-400 bg-red-500/10 border-red-500/20";
  } else if (groups.some(g => g.includes("vulnerability_detector"))) {
    category = "Vulnerability Scanner"; categoryColor = "text-orange-400 bg-orange-500/10 border-orange-500/20";
  } else if (groups.some(g => g.includes("rootcheck") || g.includes("rootkit"))) {
    category = "Rootkit Detection";     categoryColor = "text-red-500 bg-red-500/10 border-red-500/20";
  } else if (filename.includes("osquery")) {
    category = "osquery";               categoryColor = "text-teal-400 bg-teal-500/10 border-teal-500/20";
  } else if (detail?.filename) {
    const base = detail.filename.replace(/^\d+-/, "").replace(/_rules\.xml$/, "").replace(/_/g, " ");
    category = base.charAt(0).toUpperCase() + base.slice(1);
  }

  const parts: string[] = [`Fires when: ${rule.description}.`];
  if (detail?.mitre.technique.length) {
    const tactics = [...new Set(detail.mitre.tactic)].join(", ");
    parts.push(`MITRE ATT&CK: ${detail.mitre.technique.slice(0, 2).join(", ")}${tactics ? ` (${tactics})` : ""}.`);
  }
  if (groups.some(g => g.includes("syscheck")))
    parts.push("Monitors file and registry changes on protected paths.");
  else if (groups.some(g => g.includes("vulnerability_detector")))
    parts.push("Generated by Wazuh's vulnerability detection module.");
  else if (groups.some(g => g.includes("rootcheck")))
    parts.push("Generated by Wazuh's rootkit detection engine.");

  return { text: parts.join(" "), category, categoryColor };
}

function findSimilarRules(current: NoisyRule, allRules: NoisyRule[]): NoisyRule[] {
  const stop = new Set(["alert","event","rule","found","detect","attempt",
    "file","user","host","system","windows","error","failed","successful","could"]);
  const words = current.description.toLowerCase().split(/\W+/)
    .filter(w => w.length > 3 && !stop.has(w));
  if (!words.length) return [];

  return allRules
    .filter(r => r.rule_id !== current.rule_id)
    .map(r => ({ r, score: words.filter(w => r.description.toLowerCase().includes(w)).length }))
    .filter(({ score }) => score >= 2)
    .sort((a, b) => b.score - a.score || b.r.alert_count - a.r.alert_count)
    .slice(0, 5)
    .map(({ r }) => r);
}

function matchNinja(agentName: string, devices: NinjaDevice[]): NinjaDevice | null {
  const norm = (s: string) => (s ?? "").toLowerCase().split(".")[0].trim();
  const t = norm(agentName);
  return devices.find(d => norm(d.systemName ?? "") === t || norm(d.displayName ?? "") === t) ?? null;
}

function computeConcentration(agents: RuleBreakdownBucket[], total: number) {
  let cum = 0, needed = 0;
  for (const a of agents) {
    cum += a.count; needed++;
    if (total > 0 && cum / total >= 0.9) break;
  }
  return { needed, pct: total > 0 ? Math.round((cum / total) * 100) : 0 };
}

// Collapse GUID segments (e.g. VSS snapshot paths) so near-identical paths are grouped
const GUID_RE = /\{[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}\}/gi;
const HEX8_RE = /\\[0-9a-fA-F]{8,}(?=\\|$)/g;

function groupPaths(items: RuleBreakdownBucket[]): RuleBreakdownBucket[] {
  const map = new Map<string, number>();
  for (const item of items) {
    const key = item.value
      .replace(GUID_RE, "{GUID}")
      .replace(HEX8_RE, (m) => m.replace(/[0-9a-fA-F]{8,}/, "*"));
    map.set(key, (map.get(key) ?? 0) + item.count);
  }
  return Array.from(map.entries())
    .map(([value, count]) => ({ value, count }))
    .sort((a, b) => b.count - a.count);
}

